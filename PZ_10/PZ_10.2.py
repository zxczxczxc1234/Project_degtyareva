t = 0
d = 0
for i in open('стих.txt', encoding='UTF-8'):
    print(i, end='')
    t += 1
    for j in i:
        if j == ' ' or j == '\n':
            d += 1
print(end='\n')
print('Количество пробельных символов: ', d, end='\n')

